/**
 * Priority of an event handler.
 */
export enum EventHandlerPriority {
  /**
   * Lower priority handler.
   */
  Lower = 0,

  /**
   * Low priority handler.
   */
  Low = 1,

  /**
   * Normal priority handler.
   */
  Normal = 2,

  /**
   * High priority handler.
   */
  High = 3,

  /**
   * Higher priority handler.
   */
  Higher = 4,
}
